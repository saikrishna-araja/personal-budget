
var dataSource = {
    datasets: [
        {
            data: [],
            backgroundColor: [
                '#ffcd56',
                '#ff6384',
                '#36a2eb',
                '#fd6b19',
                'green',
                'yellow',
                'peach'
            ]
        }
    ],
    labels: []
};

var chartData= [];

/**
 * Function to create pie chart
 */
function createChart() {
    var ctx = document.getElementById('myChart').getContext('2d');
    var myPieChart = new Chart(ctx, {
        type: 'pie',
        data: dataSource
    });
}

/**
 * Function to retrieve the budget data from the budget API
 */
function getBudget() {
    axios.get('http://localhost:3000/budget')
    .then(function (res) {
        for (var i = 0; i < res.data.myBudget.length; i++) {
            dataSource.datasets[0].data[i] = res.data.myBudget[i].budget;
            dataSource.labels[i] = res.data.myBudget[i].title;

            var budgetItem = {
                value: res.data.myBudget[i].budget,
                label: res.data.myBudget[i].title
            };
            // Add the created object to the array
            chartData.push(budgetItem);
        }
    createChart();
    createDonutChart(chartData);
});
}
getBudget();
